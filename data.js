if ('serviceWorker' in navigator) {
  navigator.serviceWorker.register('service-worker.js')
    .then(registration => {
     
      console.log('Service Worker registered!');

      var toastSuccess = app.toast.create({
        text: '<i style="font-size:16px;color:lightgreen;" class="f7-icons ">checkmark_alt_circle_fill</i> Service Worker registered successfully.',
        closeTimeout: 2000
      });
      toastSuccess.open();
    })
    .catch(error => {
     
      console.error('<i style="font-size:16px;color:red;" class="f7-icons color-red">xmark_circle_fill</i> Service Worker registration failed:', error);

      var toastError = app.toast.create({
        text: 'Service Worker registration failed.',
        closeTimeout: 2000
      });
      toastError.open();
    });
}

// Show the infinite progress dialog
function showInfiniteProgress() {
  app.dialog.progress('Refreshing files...', 'infinite');
}

// Hide the infinite progress dialog
function hideInfiniteProgress() {
  app.dialog.close();
}

function update() {
  app.dialog.confirm('This will re-register the service-worker and update all the files.', 'Refresh app files?', function() {
    showInfiniteProgress();

    setTimeout(function() {
      hideInfiniteProgress();

      app.dialog.alert('Files successfully refreshed!', 'Success', function() {
        // Reload the page only if the user presses "OK"
        window.location.reload();
      });

    }, 3000);

    if ('serviceWorker' in navigator) {
      navigator.serviceWorker.getRegistrations().then(registrations => {
        registrations.forEach(registration => {
          registration.unregister();
        });
      }).then(() => {
         navigator.serviceWorker.register('service-worker.js')
          .then(registration => {
            console.log('Service worker re-registered:', registration);
            registration.update();
          })
          .catch(error => {
            console.error('Service worker registration failed:', error);
          });
      });
    }
  });
}
